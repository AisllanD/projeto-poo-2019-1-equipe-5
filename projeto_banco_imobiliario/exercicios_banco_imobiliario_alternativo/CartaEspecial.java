package exercicios_banco_imobiliario_alternativo;

public class CartaEspecial {

	private int id;
	private String nome;
	
	public CartaEspecial() {}
	
	public CartaEspecial(int id, String nome) {
		this.id = id;
		this.nome = nome;
	}

	public int getId() {
		return id;
	}

	public String getNome() {
		return nome;
	}
	
	
}
